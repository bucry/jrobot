package com.u1wan.core.xml;

public class ParmsEntity {

	private int parmsId;
	private String parmsName;
	private int offset;
	private int length;

	public int getParmsId() {
		return parmsId;
	}

	public void setParmsId(int parmsId) {
		this.parmsId = parmsId;
	}

	public String getParmsName() {
		return parmsName;
	}

	public void setParmsName(String parmsName) {
		this.parmsName = parmsName;
	}

	public int getOffset() {
		return offset;
	}

	public void setOffset(int offset) {
		this.offset = offset;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}

}
